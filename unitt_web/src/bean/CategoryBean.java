package bean;

/*

	이름              		널  		      유형
--------------- -------- --------------
COURSEBOARD_IDX NOT NULL NUMBER
MEMBER_ID       NOT NULL VARCHAR2(50)
NAME            NOT NULL VARCHAR2(50)
CATEGORY                 VARCHAR2(30)
TITLE           NOT NULL VARCHAR2(400)
CONTENT         NOT NULL VARCHAR2(2000)
IMAGE                    VARCHAR2(200)
SHOT_INTRODUCE           VARCHAR2(20)
PLAN                     VARCHAR2(300)

 */
public class CategoryBean {
	int courseboard_idx;
	String member_id;
	String name;
	String category;
	String title;
	String content;
	String image;
	String shot_introduce;
	String plan;

	public CategoryBean() {

	}


	public int getCourseboard_idx() {
		return courseboard_idx;
	}


	public void setCourseboard_idx(int courseboard_idx) {
		this.courseboard_idx = courseboard_idx;
	}


	public String getMember_id() {
		return member_id;
	}

	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCategory() {
		return category;
	}

	public void setCategory(String category) {
		this.category = category;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public String getShot_introduce() {
		return shot_introduce;
	}

	public void setShot_introduce(String shot_introduce) {
		this.shot_introduce = shot_introduce;
	}

	public String getPlan() {
		return plan;
	}

	public void setPlan(String plan) {
		this.plan = plan;
	}

}
